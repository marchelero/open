#!/usr/bin/env node
/**
 * build-skills-index.js - regenerate .opencode/skills/INDEX.md
 *
 * Reads all SKILL.md files in .opencode/skills subdirs, extracts the
 * description and triggers from frontmatter, and writes a sorted INDEX.md.
 *
 * Usage:
 *   node .opencode/bin/build-skills-index.js
 *   node .opencode/bin/build-skills-index.js --json   # JSON to stdout
 *
 * Zero deps, CommonJS, Windows + POSIX.
 */

const fs = require('fs');
const path = require('path');

const SKILLS_DIR = path.join(__dirname, '..', 'skills');
const OUT_MD = path.join(SKILLS_DIR, 'INDEX.md');

const CATEGORIES = [
  { name: 'Code patterns',   match: /^(coding-standards|frontend-patterns|backend-patterns)$/ },
  { name: 'API / Design',    match: /^(api-design|error-handling)$/ },
  { name: 'Process',         match: /^(git-workflow|tdd-workflow|verification-loop|intent-driven-development|task-decomposition)$/ },
  { name: 'Security',        match: /^security-review$/ },
  { name: 'Tools',           match: /^(documentation-lookup|mcp-server-patterns)$/ },
];

function categorize(name) {
  for (const cat of CATEGORIES) {
    if (cat.match.test(name)) return cat.name;
  }
  return 'Other';
}

function parseFrontmatter(content) {
  if (!content) return null;
  const stripped = content.replace(/^(?:<!--[^-]*(?:-[^-]+)*-->\s*\n)+/, '');
  if (!stripped.startsWith('---')) return null;
  const end = stripped.indexOf('\n---', 3);
  if (end === -1) return null;
  const fm = stripped.slice(3, end);
  const out = {};
  for (const line of fm.split(/\r?\n/)) {
    const m = line.match(/^([a-z_]+)\s*:\s*(.*)$/i);
    if (m) {
      let v = m[2].trim();
      if (v.startsWith('"') && v.endsWith('"')) v = v.slice(1, -1);
      if (v.startsWith("'") && v.endsWith("'")) v = v.slice(1, -1);
      if (v.startsWith('[') && v.endsWith(']')) {
        try { out[m[1].toLowerCase()] = JSON.parse(v.replace(/'/g, '"')); }
        catch { out[m[1].toLowerCase()] = v.slice(1, -1).split(',').map(s => s.trim()).filter(Boolean); }
      } else {
        out[m[1].toLowerCase()] = v;
      }
    }
  }
  return out;
}

function listSkills() {
  if (!fs.existsSync(SKILLS_DIR)) return [];
  return fs.readdirSync(SKILLS_DIR)
    .filter(f => {
      try { return fs.statSync(path.join(SKILLS_DIR, f)).isDirectory(); } catch { return false; }
    })
    .map(dirName => {
      const skillFile = path.join(SKILLS_DIR, dirName, 'SKILL.md');
      if (!fs.existsSync(skillFile)) return null;
      const stat = fs.statSync(skillFile);
      const content = fs.readFileSync(skillFile, 'utf8');
      const fm = parseFrontmatter(content) || {};
      return {
        name: dirName,
        description: (fm.description || '').trim(),
        triggers: Array.isArray(fm.triggers) ? fm.triggers : [],
        size_bytes: stat.size,
        category: categorize(dirName),
      };
    })
    .filter(Boolean);
}

function renderMarkdown(skills) {
  const total = skills.length;
  const totalSize = skills.reduce((s, x) => s + x.size_bytes, 0);

  const lines = [];
  lines.push('# Skills Index');
  lines.push('');
  lines.push('> Auto-generated by `.opencode/bin/build-skills-index.js`. Do not edit by hand.');
  lines.push('> Regenerate: `node .opencode/bin/build-skills-index.js`');
  lines.push('');
  lines.push(`**Total**: ${total} skills · ${(totalSize / 1024).toFixed(1)} KB`);
  lines.push('');

  const byCat = new Map();
  for (const s of skills) {
    if (!byCat.has(s.category)) byCat.set(s.category, []);
    byCat.get(s.category).push(s);
  }

  for (const [cat, list] of byCat) {
    list.sort((a, b) => a.name.localeCompare(b.name));
    lines.push(`## ${cat} (${list.length})`);
    lines.push('');
    lines.push('| Skill | Triggers | Size | Description |');
    lines.push('|-------|----------|-----:|-------------|');
    for (const s of list) {
      const desc = s.description.length > 100 ? s.description.slice(0, 97) + '...' : s.description;
      const descEsc = desc.replace(/\|/g, '\\|').replace(/\n/g, ' ');
      const trigStr = s.triggers.length > 0 ? s.triggers.slice(0, 6).join(', ') + (s.triggers.length > 6 ? '...' : '') : '—';
      lines.push(`| \`${s.name}\` | ${trigStr} | ${(s.size_bytes / 1024).toFixed(1)}KB | ${descEsc} |`);
    }
    lines.push('');
  }

  lines.push('---');
  lines.push('');
  lines.push('## Trigger Routing');
  lines.push('');
  lines.push('When a user request comes in, the primary agent loads the right skill based on trigger words. Use `skill-router` for the full map.');
  lines.push('');

  const allTriggers = new Map();
  for (const s of skills) {
    for (const t of s.triggers) {
      if (!allTriggers.has(t)) allTriggers.set(t, []);
      allTriggers.get(t).push(s.name);
    }
  }
  const sortedTriggers = [...allTriggers.entries()].sort((a, b) => a[0].localeCompare(b[0]));
  if (sortedTriggers.length > 0) {
    lines.push('| Trigger | Skill |');
    lines.push('|---------|-------|');
    for (const [t, names] of sortedTriggers.slice(0, 60)) {
      lines.push(`| ${t} | ${names.map(n => '`' + n + '`').join(', ')} |`);
    }
    if (sortedTriggers.length > 60) lines.push(`| ... | (${sortedTriggers.length - 60} more) |`);
    lines.push('');
  }

  lines.push('---');
  lines.push('');
  lines.push('## See also');
  lines.push('');
  lines.push('- [Agents index](../AGENTS_INDEX.md) — 69 starter-pack agents');
  lines.push('- [Commands index](../commands/) — 63 starter-pack commands');
  lines.push('- `/list-skills` — interactive catalog with filters');
  lines.push('');

  return lines.join('\n');
}

function main() {
  const args = process.argv.slice(2);
  const asJson = args.includes('--json');

  const skills = listSkills();
  skills.sort((a, b) => a.name.localeCompare(b.name));

  if (asJson) {
    process.stdout.write(JSON.stringify(skills, null, 2) + '\n');
    return;
  }

  const md = renderMarkdown(skills);
  fs.writeFileSync(OUT_MD, md, 'utf8');
  process.stdout.write(`Wrote ${OUT_MD} (${skills.length} skills)\n`);
}

main();
