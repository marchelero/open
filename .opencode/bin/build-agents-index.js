#!/usr/bin/env node
/**
 * build-agents-index.js - regenerate .opencode/AGENTS_INDEX.md
 *
 * Reads all agent .md files in .opencode/agents/, extracts the
 * description and mode from frontmatter, categorizes by filename
 * heuristics, and writes a sorted INDEX.md.
 *
 * Usage:
 *   node .opencode/bin/build-agents-index.js
 *   node .opencode/bin/build-agents-index.js --json   # JSON to stdout
 *
 * Zero deps, CommonJS, Windows + POSIX.
 */

const fs = require('fs');
const path = require('path');

const AGENTS_DIR = path.join(__dirname, '..', 'agents');
const OUT_MD = path.join(__dirname, '..', 'AGENTS_INDEX.md');

const CATEGORIES = [
  { name: 'Build / Plan',       match: /^(build|prd-agent|planner|code-architect|code-explorer|architect|migration-planner)\.md$/ },
  { name: 'Review (General)',   match: /^(code-reviewer|security-reviewer|pr-test-analyzer|refactor-cleaner|code-simplifier|comment-analyzer|silent-failure-hunter|type-design-analyzer)\.md$/ },
  { name: 'Language Reviewers', match: /(typescript|python|go|rust|java|kotlin|csharp|swift|cpp|fsharp|php|flutter|dart|react|vue|svelte)-reviewer\.md$/ },
  { name: 'Build Resolvers',    match: /-resolver\.md$/ },
  { name: 'Specialized',        match: /^(database-reviewer|fastapi-reviewer|healthcare-reviewer|harmonyos-app-resolver|marketing-agent|seo-specialist|mle-reviewer|performance-optimizer|network-architect|network-config-reviewer|network-troubleshooter|homelab-architect|docs-lookup|a11y-architect)\.md$/ },
  { name: 'Quality / Process',  match: /^(tdd-guide|e2e-runner|doc-updater|conversation-analyzer|harness-optimizer|opensource-forker|opensource-sanitizer|opensource-packager|loop-operator|report-auditor|chief-of-staff)\.md$/ },
  { name: 'Meta / Harness',     match: /^(gan-planner|gan-evaluator|gan-generator)\.md$/ },
];

function categorize(filename) {
  for (const cat of CATEGORIES) {
    if (cat.match.test(filename)) return cat.name;
  }
  return 'Other';
}

function parseFrontmatter(content) {
  // Strip leading HTML comments and blank lines, then locate the first --- block.
  const stripped = content.replace(/^(?:<!--[^-]*(?:-[^-]+)*-->\s*\n)+/, '');
  if (!stripped.startsWith('---')) return null;
  const end = stripped.indexOf('\n---', 3);
  if (end === -1) return null;
  const fm = stripped.slice(3, end);
  const out = {};
  for (const line of fm.split(/\r?\n/)) {
    const m = line.match(/^([a-z_]+):\s*(.*)$/i);
    if (m) {
      let v = m[2].trim();
      if (v.startsWith('"') && v.endsWith('"')) v = v.slice(1, -1);
      out[m[1].toLowerCase()] = v;
    }
  }
  return out;
}

function listAgents() {
  if (!fs.existsSync(AGENTS_DIR)) return [];
  return fs.readdirSync(AGENTS_DIR)
    .filter(f => f.endsWith('.md') && f !== 'INDEX.md')
    .map(f => {
      const full = path.join(AGENTS_DIR, f);
      const stat = fs.statSync(full);
      const content = fs.readFileSync(full, 'utf8');
      const fm = parseFrontmatter(content) || {};
      return {
        name: f.replace(/\.md$/, ''),
        filename: f,
        description: (fm.description || '').trim(),
        mode: (fm.mode || '').trim(),
        size_bytes: stat.size,
        category: categorize(f),
      };
    });
}

function renderMarkdown(agents) {
  const total = agents.length;
  const totalSize = agents.reduce((s, a) => s + a.size_bytes, 0);

  const lines = [];
  lines.push('# Agents Index');
  lines.push('');
  lines.push(`> Auto-generated by \`.opencode/bin/build-agents-index.js\`. Do not edit by hand.`);
  lines.push(`> Regenerate: \`node .opencode/bin/build-agents-index.js\``);
  lines.push('');
  lines.push(`**Total**: ${total} agents · ${(totalSize / 1024).toFixed(1)} KB`);
  lines.push('');

  const byCat = new Map();
  for (const a of agents) {
    if (!byCat.has(a.category)) byCat.set(a.category, []);
    byCat.get(a.category).push(a);
  }

  for (const [cat, list] of byCat) {
    list.sort((a, b) => a.name.localeCompare(b.name));
    lines.push(`## ${cat} (${list.length})`);
    lines.push('');
    lines.push('| Agent | Mode | Size | Description |');
    lines.push('|-------|------|-----:|-------------|');
    for (const a of list) {
      const desc = a.description.length > 110 ? a.description.slice(0, 107) + '...' : a.description;
      const descEsc = desc.replace(/\|/g, '\\|').replace(/\n/g, ' ');
      lines.push(`| \`${a.name}\` | ${a.mode} | ${(a.size_bytes / 1024).toFixed(1)}KB | ${descEsc} |`);
    }
    lines.push('');
  }

  lines.push('---');
  lines.push('');
  lines.push('## See also');
  lines.push('');

  // Auto-count skills and commands at index time
  const skillsDir = path.join(__dirname, '..', 'skills');
  const cmdsDir = path.join(__dirname, '..', 'commands');
  const skillCount = fs.existsSync(skillsDir) ? fs.readdirSync(skillsDir).filter(f => {
    try { return fs.statSync(path.join(skillsDir, f)).isDirectory(); } catch { return false; }
  }).length : 0;
  const cmdCount = fs.existsSync(cmdsDir) ? fs.readdirSync(cmdsDir).filter(f => f.endsWith('.md')).length : 0;

  lines.push(`- [Skills index](../skills/) — ${skillCount} starter-pack skills (api-design, coding-standards, frontend-patterns, backend-patterns, error-handling, git-workflow, intent-driven-development, mcp-server-patterns, security-review, task-decomposition, tdd-workflow, verification-loop)`);
  lines.push(`- [Commands index](../commands/) — ${cmdCount} starter-pack commands`);
  lines.push('- `/list-agents` — interactive catalog with filters');
  lines.push('');

  return lines.join('\n');
}

function main() {
  const args = process.argv.slice(2);
  const asJson = args.includes('--json');

  const agents = listAgents();
  agents.sort((a, b) => a.name.localeCompare(b.name));

  if (asJson) {
    process.stdout.write(JSON.stringify(agents, null, 2) + '\n');
    return;
  }

  const md = renderMarkdown(agents);
  fs.writeFileSync(OUT_MD, md, 'utf8');
  process.stdout.write(`Wrote ${OUT_MD} (${agents.length} agents)\n`);
}

main();
